CREATE DATABASE MI_TRABAJO;

USE MI_TRABAJO;

CREATE TABLE DEPARTAMENTO
(DEPT_NO INT PRIMARY KEY,
DNOMBRE VARCHAR(14),
LOC VARCHAR(14));

CREATE TABLE EMPLEADO(
EMP_NO INT PRIMARY KEY,
APELLIDO VARCHAR(10),
OFICIO VARCHAR(10),
DIR INT,
FECHA_ALT DATE,
SALARIO INT,
COMISION INT,
DEPT_NO INT,
FOREIGN KEY (DEPT_NO) REFERENCES DEPARTAMENTO (DEPT_NO));

